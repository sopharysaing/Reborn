    private void setBackgroundImage(){
        int width = (int) Toolkit.getDefaultToolkit().getScreenSize().getWidth();
        int height = (int) Toolkit.getDefaultToolkit().getScreenSize().getHeight();
        //System.out.println(width+" "+height);
        setLayout(new BorderLayout());
        if (width == 1280 && height == 800){
            setContentPane(new JLabel(new ImageIcon("Image\\Background\\Main Menu\\backgroundMainMenu1280x800.png")));
        } else if (width == 1366 && height == 768){
            setContentPane(new JLabel(new ImageIcon("Image\\Background\\Main Menu\\backgroundMainMenu1366x768.png")));
        } else if (width ==1920 && height== 1080) {
            setContentPane(new JLabel(new ImageIcon("Image\\Background\\Main Menu\\backgroundMainMenu1920x1080.png")));
        }
        
        
        setLayout(new FlowLayout());
    }
    
