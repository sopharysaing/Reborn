    private void tableColumn (){
        try {
            Statement statement = Data.getDataConnection().createStatement();
            ResultSet resultSetColumn = statement.executeQuery("DESCRIBE dbmajorcineplex.dbvListMovie");
            table.addRowResultSet(resultSetColumn);
            resultSetColumn.close();
            statement.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }
    private void tableRow (){
        try {
            Statement statement = Data.getDataConnection().createStatement();
            ResultSet resultSetRow = statement.executeQuery("SELECT\n" +
            "tblschedule.ScheduleID,\n" +
            "tblmovie.MovieTitle,\n" +
            "tblschedule.ShowDate,\n" +
            "tbltheatre.ThreatreName,\n" +
            "tbltime.Time,\n" +
            "tblscheduledetail.EndTime,\n" +
            "tblpromotion.PromotionName\n" +
            "FROM\n" +
            "tblschedule\n" +
            "INNER JOIN tblmovie ON tblschedule.MovieID = tblmovie.MovieID ,\n" +
            "tbltheatre ,\n" +
            "tbltime\n" +
            "INNER JOIN tblscheduledetail ON tblscheduledetail.ScheduleID = tblschedule.ScheduleID AND tblscheduledetail.TimeID = tbltime.TimeID AND tblscheduledetail.TheatreID = tbltheatre.TheatreID\n" +
            "INNER JOIN tblpromotion ON tbltime.PromotionID = tblpromotion.PromotionID");
            table.addRowResultSet(resultSetRow);
            resultSetRow.close();
            statement.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }
    
